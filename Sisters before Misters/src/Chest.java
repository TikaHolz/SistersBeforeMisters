public class Chest
	{
		private String output;
		private int money;
		
		public Chest (String o, int m)
		{
			output = o;
			money = m;
		}

		public String getOutput()
			{
				return output;
			}

		public void setOutput(String output)
			{
				this.output = output;
			}

		public int getMoney()
			{
				return money;
			}

		public void setMoney(int money)
			{
				this.money = money;
			}
		
		

	}
